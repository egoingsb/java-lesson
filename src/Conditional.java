
public class Conditional {

	public static void main(String[] args) {
		
		System.out.println(0);
		
		if(1 > 0) {
			System.out.println(1); // 출력됩니다.
		}
		
		if(1 < 0) {
			System.out.println(2); // 출력되지 않습니다. 
		}
		

		if(1>0) {
			System.out.println(3); // 출력됩니다. 
		} else {
			System.out.println(4); // 출력되지 않습니다. 
		}
		
		System.out.println(5);
		
	}

}
